# 사칙연산
print("##### 사칙 연산 #####")
print(1+1)
print(3-2)
print(5*2)
print(6/3)

# 그 외 연산
print("##### 그 외 연산 #####")
print(2**3) # 지수 연산

print(5%3) # 나머지 연산
print(10%3)

print(5//3) # 몫 연산
print(10//3)

# 논리 연산
print("##### 논리 연산 #####")
print(10 > 3) # True
print(4 >= 7) # False
print(10 < 3) # False
print( 5 <= 5) # True

print( 3 == 3)
print( 3 + 4 == 7)
print( 1 != 3) # True
print(not(1 != 3)) # False

print("##### AND/OR 연산 #####")
print((3 > 0) and (3 < 5)) # True AND 연산
print((3 > 0) & (3 < 5)) # True AND 연산
print((3 > 0) or (3 > 5)) # True OR 연산
print((3 > 0) | (3 > 5)) # True OR 연산

