# 숫자 처리 함수
# 절댓값 abs
print(abs(-5)) # 5

# 지수 pow
print(pow(4, 2)) # 4 ^ 2 = 16

# 최대 최소 max min
print(max(5, 12)) # 12
print(min(5, 12)) # 5

# 반올림 round
print(round(3.14)) # 3
print(round(4.899)) # 4

# 수학 연산 라이브러리
from math import *

# 내림 floor
print(floor(4.99)) # 4
# 올림 ceil
print(ceil(4.99)) #5
# 제곱근 sqrt
print(sqrt(16)) # 4