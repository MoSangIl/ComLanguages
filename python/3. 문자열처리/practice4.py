## 탈출문자를 사용하여 따옴표를 나타내자
# 저는 "모상일" 입니다.
print("저는 '모상일' 입니다.")
print('저는 "모상일" 입니다.')
print("저는 \"모상일\" 입니다.")

# \\ : 문장 내에서 \
# 경로를 출력해 보자.
